package Model;

/**
 *
 * @author VIET
 */
public class Employee {
    
    String empName;
    String id;
    String phone;
    int salary;

    public Employee() {
    }

    public Employee(String empName, String id, String phone, int salary) {
        this.empName = empName;
        this.id = id;
        this.phone = phone;
        this.salary = salary;
    }

    public String getEmpName() {
        return empName;
    }

    public void setEmpName(String empName) {
        this.empName = empName;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public int getSalary() {
        return salary;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }

    

    
}
